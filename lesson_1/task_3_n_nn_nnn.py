"""
Домашнее задание 1. Задание 3.
Пользователь вводит одну цифру n (1..9).
Нужно посчитать сумму n + nn + nnn.
Пример: n = 3  ->  3 + 33 + 333 = 369.
"""

user_input = input("Введите одну цифру от 1 до 9: ")

# Проверка, что введена ровно одна цифра в диапазоне 1-9.
if not user_input.isdigit() or len(user_input) != 1 or user_input == "0":
    print("Ошибка: нужно ввести одну цифру от 1 до 9.")
else:
    n = int(user_input)

    # Собираем двузначное и трёхзначное число из той же цифры.
    nn = int(user_input * 2)   # "3" * 2 -> "33" -> 33
    nnn = int(user_input * 3)  # "3" * 3 -> "333" -> 333

    total = n + nn + nnn

    print(f"{n} + {nn} + {nnn} = {total}")
