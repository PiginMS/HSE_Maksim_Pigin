"""
Домашнее задание 1. Задание 2.
Пользователь вводит время в секундах.
Нужно рассчитать часы, минуты и секунды, сохранить в отдельные
переменные и вывести в консоль.

Используются:
- приведение типа str -> int,
- строковый метод .isdigit() для проверки, что ввод — только цифры.
"""

user_input = input("Введите количество секунд (целое положительное число): ")

# Проверяем, что пользователь ввёл только цифры.
if not user_input.isdigit():
    print("Ошибка: нужно ввести целое положительное число из цифр.")
else:
    total_seconds = int(user_input)

    # Целочисленное деление и остаток от деления
    hours = total_seconds // 3600
    minutes = (total_seconds % 3600) // 60
    seconds = total_seconds % 60

    print()
    print("Вы ввели всего секунд:", total_seconds)
    print("Часов :", hours)
    print("Минут :", minutes)
    print("Секунд:", seconds)
    print(f"Итого: {hours} ч {minutes} мин {seconds} сек")
